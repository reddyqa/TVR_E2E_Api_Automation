package com.MSIL.POJO;

public class enquiry {
	
	private String type;
	private String  dealerCode;
	private String  enquirySource;
	private String  newCarModel;
	private String  newCarVariant;
	private String mspin;
	
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDealerCode() {
		return dealerCode;
	}
	public void setDealerCode(String dealerCode) {
		this.dealerCode = dealerCode;
	}
	public String getEnquirySource() {
		return enquirySource;
	}
	public void setEnquirySource(String enquirySource) {
		this.enquirySource = enquirySource;
	}
	public String getNewCarModel() {
		return newCarModel;
	}
	public void setNewCarModel(String newCarModel) {
		this.newCarModel = newCarModel;
	}
	public String getNewCarVariant() {
		return newCarVariant;
	}
	public void setNewCarVariant(String newCarVariant) {
		this.newCarVariant = newCarVariant;
	}
	public String getMspin() {
		return mspin;
	}
	public void setMspin(String mspin) {
		this.mspin = mspin;
	}

}
