package com.MSIL.POJO.Certification.RatingAndPricing_POJO;

public class vehicleRating {
	
	
	
	
	private String exterior;
	private String interior;
	private String functions;
	private String frames;
	private String engine;
	private String finalVehicleRating;
	
	
	public String getExterior() {
		return exterior;
	}
	public void setExterior(String exterior) {
		this.exterior = exterior;
	}
	public String getInterior() {
		return interior;
	}
	public void setInterior(String interior) {
		this.interior = interior;
	}
	public String getFunctions() {
		return functions;
	}
	public void setFunctions(String functions) {
		this.functions = functions;
	}
	public String getFrames() {
		return frames;
	}
	public void setFrames(String frames) {
		this.frames = frames;
	}
	public String getEngine() {
		return engine;
	}
	public void setEngine(String engine) {
		this.engine = engine;
	}
	public String getFinalVehicleRating() {
		return finalVehicleRating;
	}
	public void setFinalVehicleRating(String finalVehicleRating) {
		this.finalVehicleRating = finalVehicleRating;
	}
	
	
	

}
