package com.MSIL.POJO;

public class CustomerOccupationSavePOJO
{
	private String occupationTypeCode;
	private String employerType;
	private String companyName;
	private String grossIncome;
	private String netIncome;
	private String workEx;
	private String validFrom;
	private String validTill;
	
	
	public String getOccupationTypeCode() {
		return occupationTypeCode;
	}
	public void setOccupationTypeCode(String occupationTypeCode) {
		this.occupationTypeCode = occupationTypeCode;
	}
	public String getEmployerType() {
		return employerType;
	}
	public void setEmployerType(String employerType) {
		this.employerType = employerType;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getGrossIncome() {
		return grossIncome;
	}
	public void setGrossIncome(String grossIncome) {
		this.grossIncome = grossIncome;
	}
	public String getNetIncome() {
		return netIncome;
	}
	public void setNetIncome(String netIncome) {
		this.netIncome = netIncome;
	}
	public String getWorkEx() {
		return workEx;
	}
	public void setWorkEx(String workEx) {
		this.workEx = workEx;
	}
	public String getValidFrom() {
		return validFrom;
	}
	public void setValidFrom(String validFrom) {
		this.validFrom = validFrom;
	}
	public String getValidTill() {
		return validTill;
	}
	public void setValidTill(String validTill) {
		this.validTill = validTill;
	}
	
	
}
