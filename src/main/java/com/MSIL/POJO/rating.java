package com.MSIL.POJO;

public class rating {
	
	private vehicleRating vehicleRating;
	private String drivingExperience;
	private String evaluatorRating;
	private String otherComment;

	public vehicleRating getVehicleRating() {
		return vehicleRating;
	}
	public void setVehicleRating(vehicleRating vehicleRating) {
		this.vehicleRating = vehicleRating;
	}
	public String getDrivingExperience() {
		return drivingExperience;
	}
	public void setDrivingExperience(String drivingExperience) {
		this.drivingExperience = drivingExperience;
	}
	public String getEvaluatorRating() {
		return evaluatorRating;
	}
	public void setEvaluatorRating(String evaluatorRating) {
		this.evaluatorRating = evaluatorRating;
	}
	public String getOtherComment() {
		return otherComment;
	}
	public void setOtherComment(String otherComment) {
		this.otherComment = otherComment;
	}
	
}
