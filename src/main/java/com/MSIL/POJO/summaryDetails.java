package com.MSIL.POJO;

public class summaryDetails {
	
	private String defectPanel;	
	private String  defectType	;
	private String 	defectintensity	;
	private String evaluatorRfCost	;
	private String cqRfCost	;
	
	public String getDefectPanel() {
		return defectPanel;
	}
	public void setDefectPanel(String defectPanel) {
		this.defectPanel = defectPanel;
	}
	public String getDefectType() {
		return defectType;
	}
	public void setDefectType(String defectType) {
		this.defectType = defectType;
	}
	public String getDefectintensity() {
		return defectintensity;
	}
	public void setDefectintensity(String defectintensity) {
		this.defectintensity = defectintensity;
	}
	public String getEvaluatorRfCost() {
		return evaluatorRfCost;
	}
	public void setEvaluatorRfCost(String evaluatorRfCost) {
		this.evaluatorRfCost = evaluatorRfCost;
	}
	public String getCqRfCost() {
		return cqRfCost;
	}
	public void setCqRfCost(String cqRfCost) {
		this.cqRfCost = cqRfCost;
	}


}
