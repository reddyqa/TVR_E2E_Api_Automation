package com.MSIL.POJO;

public class GetVehicleDetailsPOJO {
	
	
	private String regNo;
	private String vinNo;
	private String p_pmc;
	private String p_model;
	private String p_chassis;
	
	public String getVinNo() {
		return vinNo;
	}

	public void setVinNo(String vinNo) {
		this.vinNo = vinNo;
	}

	public String getP_pmc() {
		return p_pmc;
	}

	public void setP_pmc(String p_pmc) {
		this.p_pmc = p_pmc;
	}

	public String getP_model() {
		return p_model;
	}

	public void setP_model(String p_model) {
		this.p_model = p_model;
	}

	public String getP_chassis() {
		return p_chassis;
	}

	public void setP_chassis(String p_chassis) {
		this.p_chassis = p_chassis;
	}

	

	public String getRegNo() {
		return regNo;
	}

	public void setRegNo(String regNo) {
		this.regNo = regNo;
	}

	

}
