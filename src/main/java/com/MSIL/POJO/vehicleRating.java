package com.MSIL.POJO;

public class vehicleRating {
	private Integer exterior;
	private Integer interior;
	private Integer functions;
	private Integer frames;
	private Integer engine;
	private Integer finalVehicleRating;
	public Integer getExterior() {
		return exterior;
	}
	public void setExterior(Integer exterior) {
		this.exterior = exterior;
	}
	public Integer getInterior() {
		return interior;
	}
	public void setInterior(Integer interior) {
		this.interior = interior;
	}
	public Integer getFunctions() {
		return functions;
	}
	public void setFunctions(Integer functions) {
		this.functions = functions;
	}
	public Integer getFrames() {
		return frames;
	}
	public void setFrames(Integer frames) {
		this.frames = frames;
	}
	public Integer getEngine() {
		return engine;
	}
	public void setEngine(Integer engine) {
		this.engine = engine;
	}
	public Integer getFinalVehicleRating() {
		return finalVehicleRating;
	}
	public void setFinalVehicleRating(Integer finalVehicleRating) {
		this.finalVehicleRating = finalVehicleRating;
	}
	
	
	

}
